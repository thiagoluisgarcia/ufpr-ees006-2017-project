  private void tableClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableClienteMouseClicked

    clickedLine = tableCliente.rowAtPoint(evt.getPoint());

    Cliente cliente = modelCliente.getCliente(clickedLine);

    this.fieldCodigo.setText(String.valueOf(cliente.getId()));
    this.fieldCpf.setText(cliente.getCpf());
    this.fieldNome.setText(cliente.getNome());
    this.fieldSobrenome.setText(cliente.getSobrenome());
    
  }//GEN-LAST:event_tableClienteMouseClicked

  private void btNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNovoActionPerformed

    Cliente cliente = new Cliente();
    ClienteDAO clienteDAO = new ClienteDAO();

    try {

      cliente.setId(clienteDAO.getMax());
      cliente.setCpf(fieldCpf.getText());
      cliente.setNome(fieldNome.getText());
      cliente.setSobrenome(fieldSobrenome.getText());

      clienteDAO.insert(cliente);

    } catch (Exception ex) {

      JOptionPane.showMessageDialog(null, "Erro ao inserir no banco de dados. E=" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);

    }

    listarCliente();
  }//GEN-LAST:event_btNovoActionPerformed

  private void btAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAtualizarActionPerformed

    if (clickedLine != -1) {

      Cliente cliente = modelCliente.getCliente(clickedLine);

      cliente.setCpf(fieldCpf.getText());
      cliente.setNome(fieldNome.getText());
      cliente.setSobrenome(fieldSobrenome.getText());

      ClienteDAO dao = null;

      try {

        dao = new ClienteDAO();
        dao.update(cliente);

      } catch (Exception ex) {

        JOptionPane.showMessageDialog(null, "Erro ao atualizar no banco de dados.", "Erro", JOptionPane.ERROR_MESSAGE);

      }

      listarCliente();

    }
  }//GEN-LAST:event_btAtualizarActionPerformed

  private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed

    try {

      ClienteDAO dao = new ClienteDAO();

      int[] selectedRows = tableCliente.getSelectedRows();

      List<Cliente> toDelList = new ArrayList();

      for (int i = 0; i < selectedRows.length; i++) {

        Cliente cliente = modelCliente.getCliente(selectedRows[i]);
        dao.delete(cliente);
        toDelList.add(cliente);

      }

      listarCliente();

    } catch (Exception ex) {
      JOptionPane.showMessageDialog(null, "Erro ao realizar exclusÃ£o de Cliente", "Erro", JOptionPane.ERROR_MESSAGE);
    }
  }//GEN-LAST:event_btExcluirActionPerformed

  private void listarCliente() {

    try {

      ClienteDAO clienteDAO = new ClienteDAO();

      List<Cliente> listCliente;
      listCliente = clienteDAO.getList();
      modelCliente.setListClientes(listCliente);

    } catch (Exception ex) {

      JOptionPane.showMessageDialog(null, "Erro ao conectar com o banco de dados.", "Erro", JOptionPane.ERROR_MESSAGE);

    }

    clearScreen();

  }

  private void clearScreen() {

    this.fieldCodigo.setText("");
    this.fieldCpf.setText("");
    this.fieldNome.setText("");
    this.fieldSobrenome.setText("");

  }
  
